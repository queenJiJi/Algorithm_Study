# 개발팀은 매출전표(거래를 증빙하는 표)로 부터 주문 금액, 비과세 금액, 그리고 봉사료를 알 수 있다고 가정하겠습니다. 
# 이 정보를 바탕으로 부가가치세를 계산하는 함수를 구현해 주세요.

import math
def calculate_vat(orderAmount, taxFreeAmount, serviceFee):
  # 부가가치세 = VAT : 과세금액의 10% (과세금액: 공급가액 - 비과세 금액 )  
  # 공급대가 = 주문금액 - 봉사료(있는 경우) = 공급가액 + 부가가치세 
  # 공급대가 - 비과세금액 = 1 이라면? 부가가치세 = 0
  supply_value = orderAmount - serviceFee
  taxable_amount = supply_value - taxFreeAmount

  if taxable_amount == 1 or taxable_amount <=0 :
    return 0
  
  vat = math.ceil(taxable_amount*0.1)
  return vat

