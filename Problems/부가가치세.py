# - **회계팀** : 공급가액에서 비과세 금액을 빼주시면 됩니다. 비과세 금액은 말 그대로 부가가치세가 부과되지 않는 금액을 의미해요.
# - **개발팀** : 공급가액과 계산된 부가가치세를 합하면 주문금액과 동일한게 맞나요?
